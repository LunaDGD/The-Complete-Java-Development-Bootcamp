public class WholeNumbers {
    public static void main(String[] args) {
        int numberOfFans = 5000;
        System.out.println("There are " + numberOfFans + " fans at the games");
        // add the L at the end of a number
        long globalPopulation = 7800000000L;
        System.out.println("There are " + globalPopulation + " people on earth");

    }
    
}
