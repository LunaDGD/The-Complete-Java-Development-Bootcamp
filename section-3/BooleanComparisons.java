
public class BooleanComparisons {
    public static void main(String[] args) {
        int chemGrade = 95;
        int bioGrade = 74;
        System.out.println(bioGrade > chemGrade);

        double sales = 324.234;
        double cost = 234.134;
        System.out.println(sales < cost);

        /*
             > , Greater than 
             < , Less than
             >= Greater than or equal to 
             <= less than or equal to
             == equal to
             != not equal to 
             equals , equal to(for strings)
             !equals , not equal to (for strings)
         */
    }
}
